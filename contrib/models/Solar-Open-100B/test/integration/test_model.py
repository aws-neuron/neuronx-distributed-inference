def test_model_loads(compiled_model):
    """Test that model loads successfully (smoke test)."""
    assert compiled_model is not None
    assert hasattr(compiled_model, "config")
    print("PASS: Smoke test - Model loaded successfully")


def test_logit_accuracy(compiled_model, tokenizer, generation_config, cpu_reference):
    """
    Validate Neuron model logits against CPU HuggingFace reference.

    Uses pre-computed CPU reference logits and check_accuracy_logits_v2 which:
    - Wraps the Neuron model in HuggingFaceGenerationAdapter
    - Calls logit_validation with teacher forcing
    - Compares logits at multiple top-k tiers (top-5, top-50, top-1000, all)
    - Raises LogitMatchingValidationError on failure
    """
    expected_logits = cpu_reference["expected_logits"]
    input_ids = cpu_reference["input_ids"]
    attention_mask = torch.ones_like(input_ids)

    print(f"  Reference logits shape: {expected_logits.shape}")
    print(f"  Prompt: '{cpu_reference.get('prompt', 'N/A')}'")

    # check_accuracy_logits_v2 wraps the Neuron model in HuggingFaceGenerationAdapter
    # and runs logit_validation with teacher forcing against the reference logits.
    check_accuracy_logits_v2(
        neuron_model=compiled_model,
        expected_logits=expected_logits,
        inputs_input_ids=input_ids,
        inputs_attention_mask=attention_mask,
        generation_config=generation_config,
        num_tokens_to_check=NUM_TOKENS_TO_CHECK,
        divergence_difference_tol=DIVERGENCE_DIFFERENCE_TOL,
        tol_map=TOLERANCE_MAP,
        tokenizer=tokenizer,
    )

    print(f"PASS: Logit accuracy validated ({NUM_TOKENS_TO_CHECK} tokens)")


def test_performance_cte(compiled_model, tokenizer):
    """Measure CTE (prefill) latency."""
    prompt = "The capital of France is"
    input_ids = tokenizer(prompt, return_tensors="pt").input_ids
    seq_len = input_ids.shape[1]

    def run_cte():
        compiled_model.forward(
            input_ids=input_ids,
            attention_mask=torch.ones_like(input_ids),
            position_ids=torch.arange(seq_len, dtype=torch.int32).unsqueeze(0),
            seq_ids=torch.zeros(1, dtype=torch.int32),
        )

    # Warmup
    for _ in range(2):
        run_cte()

    # Measure
    times = []
    for _ in range(5):
        t0 = time.perf_counter()
        run_cte()
        times.append((time.perf_counter() - t0) * 1000)

    avg_ms = sum(times) / len(times)
    print(f"  CTE latency: {avg_ms:.1f} ms (avg of 5 runs)")
    # Sanity check: CTE should complete in under 2 seconds
    assert avg_ms < 2000, f"CTE too slow: {avg_ms:.1f} ms"
    print(f"PASS: CTE latency {avg_ms:.1f} ms")


def test_performance_tkg(compiled_model, tokenizer):
    """Measure TKG (decode) latency."""
    prompt = "The capital of France is"
    input_ids = tokenizer(prompt, return_tensors="pt").input_ids
    seq_len = input_ids.shape[1]

    # CTE first to set up KV cache
    output = compiled_model.forward(
        input_ids=input_ids,
        attention_mask=torch.ones_like(input_ids),
        position_ids=torch.arange(seq_len, dtype=torch.int32).unsqueeze(0),
        seq_ids=torch.zeros(1, dtype=torch.int32),
    )
    logits = (output.logits if hasattr(output, "logits") else output[0])[0, -1, :]
    prev_token = torch.argmax(logits.float().cpu()).item()
    cur_pos = seq_len

    def run_tkg(token_id, pos):
        return compiled_model.forward(
            input_ids=torch.tensor([[token_id]], dtype=torch.int64),
            attention_mask=torch.ones(1, pos + 1, dtype=torch.int64),
            position_ids=torch.tensor([[pos]], dtype=torch.int32),
            seq_ids=torch.zeros(1, dtype=torch.int32),
        )

    # Warmup TKG
    for i in range(3):
        out = run_tkg(prev_token, cur_pos + i)
        logits = (out.logits if hasattr(out, "logits") else out[0])[0, -1, :]
        prev_token = torch.argmax(logits.float().cpu()).item()

    cur_pos += 3

    # Measure
    times = []
    for i in range(10):
        t0 = time.perf_counter()
        out = run_tkg(prev_token, cur_pos + i)
        times.append((time.perf_counter() - t0) * 1000)
        logits = (out.logits if hasattr(out, "logits") else out[0])[0, -1, :]
        prev_token = torch.argmax(logits.float().cpu()).item()

    avg_ms = sum(times) / len(times)
    tok_per_sec = 1000 / avg_ms
    print(f"  TKG latency: {avg_ms:.1f} ms ({tok_per_sec:.1f} tok/s)")
    # Sanity check: TKG should complete in under 100 ms
    assert avg_ms < 100, f"TKG too slow: {avg_ms:.1f} ms"
    print(f"PASS: TKG latency {avg_ms:.1f} ms ({tok_per_sec:.1f} tok/s)")


