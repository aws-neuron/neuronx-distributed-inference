CPU_REFERENCE_LOGITS_PATH = "/mnt/models/solar_cpu_reference_logits.pt"

# Logit validation settings
NUM_TOKENS_TO_CHECK = 16
# Solar Open 100B is a 102.6B MoE model with TP=64 and BF16, which produces higher
# per-token logit relative errors than smaller dense models. The greedy token sequence
# matches exactly (no divergence), but individual logit values have ~5-10% relative
# error from accumulated BF16 rounding across 48 MoE layers and 64 tensor-parallel
# shards. Cosine similarity between CPU and Neuron logit vectors is >0.999.
DIVERGENCE_DIFFERENCE_TOL = 0.02
TOLERANCE_MAP = {
    5: (1e-5, 0.06),  # Top-5: observed max 0.051
    50: (1e-5, 0.08),  # Top-50: observed max 0.072
    1000: (1e-5, 0.08),  # Top-1000: observed max 0.072
    None: (1e-5, 0.12),  # All tokens: observed max 0.097
}


def create_model(model_path: str, compiled_path: str):
    """Create and configure the Solar Open model for inference."""
    with open(f"{model_path}/config.json") as f:
        hf_config = json.load(f)

    neuron_config = MoENeuronConfig(
        tp_degree=64,
        batch_size=1,
        seq_len=4096,
        n_active_tokens=4096,
        torch_dtype=torch.bfloat16,
        # Attention NKI kernels (34% TKG improvement)
        fused_qkv=True,
        qkv_kernel_enabled=True,
        qkv_nki_kernel_enabled=True,
        # MoE NKI kernels must be disabled (I/tp=20 too small)
        moe_fused_nki_kernel_enabled=False,
        expert_mlp_nki_kernel_enabled=False,
    )

    def load_config(c):
        for k, v in hf_config.items():
            setattr(c, k, v)

    config = SolarOpenInferenceConfig(
        neuron_config=neuron_config, load_config=load_config
    )
    model = NeuronSolarOpenForCausalLM(model_path, config)
    return model


def load_cpu_reference_logits(path: str):
    """
    Load pre-computed CPU reference logits for logit validation.

    The reference logits are generated by running the HuggingFace SolarOpenForCausalLM
    model on CPU with model.generate(output_scores=True, do_sample=False). This requires
    transformers >= 5.0 which includes the solar_open model type.

    To regenerate reference logits:
        pip install 'transformers>=5.0' accelerate
        python -c "
        import torch
        from transformers import AutoModelForCausalLM, AutoTokenizer, GenerationConfig
        model = AutoModelForCausalLM.from_pretrained(
            '/mnt/models/Solar-Open-100B-weights', torch_dtype=torch.bfloat16,
            device_map='cpu', low_cpu_mem_usage=True)
        tokenizer = AutoTokenizer.from_pretrained('/mnt/models/Solar-Open-100B-weights')
        input_ids = tokenizer.encode('The capital of France is', return_tensors='pt')
        gen_config = GenerationConfig(do_sample=False, max_new_tokens=16, min_new_tokens=16,
            eos_token_id=tokenizer.eos_token_id,
            pad_token_id=tokenizer.pad_token_id or tokenizer.eos_token_id)
        outputs = model.generate(input_ids, generation_config=gen_config,
            return_dict_in_generate=True, output_scores=True)
        expected_logits = torch.stack(outputs.scores)[:16, :, :]
        torch.save({'expected_logits': expected_logits, 'input_ids': input_ids,
            'prompt': 'The capital of France is', 'num_tokens': 16},
            '/mnt/models/solar_cpu_reference_logits.pt')
        "
    """
    if not os.path.exists(path):
        raise FileNotFoundError(
            f"CPU reference logits not found at {path}. "
            f"See docstring for generation instructions."
        )
    data = torch.load(path, weights_only=True)
    return data


